from pathlib import Path

from app.config import Settings
from app.models import WordRecord
from app.page_renderer import write_word_page


def test_write_word_page_includes_key_content(tmp_path: Path) -> None:
    word = WordRecord(
        id=1,
        term="Haus",
        display_prefix="das",
        translation_text="house",
        explanation_text="A building",
        part_of_speech="noun",
        example_source="Das Haus ist alt.",
        example_target="The house is old.",
        source="test",
        tags="home",
        difficulty=1,
        times_shown=0,
        last_shown_at=None,
        created_at="",
        updated_at="",
        is_active=1,
    )
    settings = Settings(
        project_root=tmp_path,
        profiles_dir=tmp_path / "profiles",
        profile_name="german",
        source_language_name="German",
        source_language_code="de-DE",
        target_language_name="English",
        db_path=tmp_path / "profiles" / "german" / "vocab.db",
        default_csv_path=tmp_path / "data" / "german.csv",
        detail_pages_dir=tmp_path / "pages",
        notification_title_prefix="",
        max_body_length=220,
        include_example=True,
        min_hours_between_repeats=8,
        render_translation=True,
        render_explanation=False,
    )

    page_path = write_word_page(tmp_path, word, settings)

    html = page_path.read_text(encoding="utf-8")
    assert page_path.name == "das-haus.html"
    assert "das Haus" in html
    assert "house" in html
    assert "Das Haus ist alt." in html
    assert "The house is old." in html
    assert "A building" not in html
    assert 'id="pronounce-button"' in html
    assert 'id="pronounce-example-button"' in html
    assert "SpeechSynthesisUtterance" in html
    assert '"Haus"' in html
    assert '"Das Haus ist alt."' in html
    assert '"de-DE"' in html
    assert "Generated by Vocab Notifier" in html
